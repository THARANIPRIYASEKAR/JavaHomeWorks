package org.example.example.e56;

public class E56ForLoop {
    public static void main(String[] args) {

        /*For loop that prints out even numbers from 2 to 14 using if condition
          Must include the number 14 in the output*/

        for (int i = 1; i <= 14; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }

    }
}
