package org.example.example.e60;

public class E60ForLoop {
    public static void main(String[] args) {

        // Use a for loop that starts with i = 10 and continues while i >= 1
        // Inside the loop, print the value of i

        for (int countDown = 10; countDown >= 1; countDown--) {
            System.out.println(countDown);
        }

        // After the loop, print "Happy New Year!"
        System.out.println("Happy New Year!");

    }
}
