package org.example.e57;

public class E57ForLoop {
    public static void main(String[] args) {

        /*prints odd numbers from 5 to 21 using a for loop
          that starts with i equal to 5 and increments i by 2 each iteration*/

        for (int i = 5; i < 22; i += 2) {

            System.out.println(i);

        }

    }
}
